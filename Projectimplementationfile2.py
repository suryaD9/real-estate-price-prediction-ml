# 4.4.1 — PredictionEngine class (production-ready)

class PredictionEngine:
    """
    Real-time property price prediction engine.
    Powers the Streamlit dashboard interface described in §3.9.
    """

    CONDITION_MAP  = {'Poor': 0, 'Fair': 1, 'Average': 2, 'Good': 3, 'Very Good': 4}
    WATERFRONT_MAP = {'Y': 1, 'N': 0, True: 1, False: 0, 1: 1, 0: 0}

    def __init__(self, model_dir='models'):
        self._arts = load_prediction_system(model_dir)
        self._lr     = self._arts['lr']
        self._rf     = self._arts['rf']
        self._gb     = self._arts['gb']
        self._scaler = self._arts['scaler']
        self._feats  = self._arts['features']

    def _build_frame(self, inputs: dict) -> pd.DataFrame:
        """Validate, align, and coerce inputs into model-ready DataFrame."""
        # Map string categoricals if present
        if isinstance(inputs.get('condition'), str):
            inputs['condition'] = self.CONDITION_MAP.get(inputs['condition'], 2)
        if 'waterfront' in inputs:
            inputs['waterfront'] = self.WATERFRONT_MAP.get(inputs['waterfront'], 0)
        df = pd.DataFrame([inputs])
        for f in self._feats:
            if f not in df.columns:
                df[f] = 0
        return df[self._feats].astype(float)

    def predict(self, inputs: dict) -> dict:
        """
        Returns price predictions from all three models plus ensemble average.

        Parameters
        ----------
        inputs : dict  — property feature values

        Returns
        -------
        dict with keys: lr, rf, gb, ensemble, low, high, spread
        """
        df      = self._build_frame(inputs)
        df_s    = self._scaler.transform(df)
        p_lr    = float(self._lr.predict(df_s)[0])
        p_rf    = float(self._rf.predict(df)[0])
        p_gb    = float(self._gb.predict(df)[0])
        ens     = np.mean([p_lr, p_rf, p_gb])
        return {
            'Linear Regression':        round(p_lr, 2),
            'Random Forest':            round(p_rf, 2),
            'Gradient Boosting':        round(p_gb, 2),
            'Ensemble Average':         round(ens, 2),
            'low':                      round(min(p_lr, p_rf, p_gb), 2),
            'high':                     round(max(p_lr, p_rf, p_gb), 2),
            'spread':                   round(max(p_lr, p_rf, p_gb) - min(p_lr, p_rf, p_gb), 2),
        }

