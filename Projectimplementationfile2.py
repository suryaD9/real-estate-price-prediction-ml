# 4.2.1 — PropertyPricePipeline class
# Encapsulates the full data-to-model workflow in a single reusable object

class PropertyPricePipeline:
    """
    End-to-end pipeline for real-time property price prediction.
    Implements the methodology described in Chapter 3 of the dissertation.

    Stages:
        1. Data loading and validation
        2. Preprocessing (cleaning, encoding, scaling, outlier removal)
        3. Feature selection (correlation + RF importance)
        4. Model training (Linear Regression, Random Forest, Gradient Boosting)
        5. Evaluation (MAE, RMSE, R²)
        6. Serialisation
    """

    CONDITION_MAP = {'Poor': 0, 'Fair': 1, 'Average': 2, 'Good': 3, 'Very Good': 4}

    def __init__(self, data_path: str, model_dir: str = 'models',
                 test_size: float = 0.2, random_state: int = 42,
                 top_n_features: int = 15, iqr_multiplier: float = 3.0):
        self.data_path       = data_path
        self.model_dir       = model_dir
        self.test_size       = test_size
        self.random_state    = random_state
        self.top_n_features  = top_n_features
        self.iqr_multiplier  = iqr_multiplier

        self.df_raw          = None
        self.df_clean        = None
        self.top_features    = None
        self.scaler          = StandardScaler()
        self.models          = {}
        self.results         = {}
        self.X_train = self.X_test = self.y_train = self.y_test = None

    # ── Stage 1: Load ────────────────────────────────────────────────────
    def load(self):
        self.df_raw = pd.read_csv(self.data_path)
        print(f"[1/6] Data loaded       : {self.df_raw.shape[0]:,} rows × {self.df_raw.shape[1]} cols")
        return self

    # ── Stage 2: Preprocess ──────────────────────────────────────────────
    def preprocess(self):
        df = self.df_raw.copy()
        df.drop(columns=['id'], errors='ignore', inplace=True)
        df.drop_duplicates(inplace=True)

        # Date features
        df['sale_year']  = pd.to_datetime(df['date'], format='%Y%m%dT%H%M%S', errors='coerce').dt.year
        df['sale_month'] = pd.to_datetime(df['date'], format='%Y%m%dT%H%M%S', errors='coerce').dt.month
        df.drop(columns=['date'], errors='ignore', inplace=True)

        # Encode
        df['waterfront'] = df['waterfront'].map({'Y': 1, 'N': 0})
        df['condition']  = df['condition'].map(self.CONDITION_MAP)

        # Impute
        for col in df.columns:
            if df[col].dtype in ['float64', 'int64']:
                df[col].fillna(df[col].median(), inplace=True)

        # Outlier removal (IQR)
        n_before = len(df)
        for col in ['price', 'sqft_living', 'sqft_lot', 'bedrooms', 'bathrooms']:
            Q1, Q3 = df[col].quantile(0.25), df[col].quantile(0.75)
            IQR = Q3 - Q1
            df = df[(df[col] >= Q1 - self.iqr_multiplier * IQR) &
                    (df[col] <= Q3 + self.iqr_multiplier * IQR)]

        self.df_clean = df.reset_index(drop=True)
        print(f"[2/6] Preprocessing done: {len(self.df_clean):,} rows (removed {n_before - len(self.df_clean)} outliers)")
        return self

    # ── Stage 3: Feature selection ───────────────────────────────────────
    def select_features(self):
        X = self.df_clean.drop(columns=['price'])
        y = self.df_clean['price']
        X_tr, _, y_tr, _ = train_test_split(X, y, test_size=self.test_size, random_state=self.random_state)
        rf_sel = RandomForestRegressor(n_estimators=100, random_state=self.random_state, n_jobs=-1)
        rf_sel.fit(X_tr, y_tr)
        imp = pd.Series(rf_sel.feature_importances_, index=X.columns).sort_values(ascending=False)
        self.top_features = imp.head(self.top_n_features).index.tolist()
        print(f"[3/6] Feature selection : top {self.top_n_features} features selected")
        print(f"       Top 5: {self.top_features[:5]}")
        return self

    # ── Stage 4: Train ───────────────────────────────────────────────────
    def train(self):
        X = self.df_clean[self.top_features]
        y = self.df_clean['price']
        self.X_train, self.X_test, self.y_train, self.y_test = train_test_split(
            X, y, test_size=self.test_size, random_state=self.random_state)

        X_tr_s = self.scaler.fit_transform(self.X_train)
        X_ts_s = self.scaler.transform(self.X_test)

        model_defs = {
            'Linear Regression': (LinearRegression(), X_tr_s, X_ts_s),
            'Random Forest':     (RandomForestRegressor(n_estimators=200, max_depth=20,
                                   min_samples_split=5, random_state=self.random_state, n_jobs=-1),
                                  self.X_train, self.X_test),
            'Gradient Boosting': (GradientBoostingRegressor(n_estimators=300, learning_rate=0.05,
                                   max_depth=5, subsample=0.8, random_state=self.random_state),
                                  self.X_train, self.X_test),
        }

        for name, (model, X_tr, X_ts) in model_defs.items():
            t0 = time.time()
            model.fit(X_tr, self.y_train)
            elapsed = time.time() - t0
            preds = model.predict(X_ts)
            self.models[name] = model
            self.results[name] = {
                'MAE':   mean_absolute_error(self.y_test, preds),
                'RMSE':  np.sqrt(mean_squared_error(self.y_test, preds)),
                'R2':    r2_score(self.y_test, preds),
                'time':  elapsed,
                'preds': preds,
            }
            print(f"[4/6] {name:22s}: R²={self.results[name]['R2']:.4f}  MAE=${self.results[name]['MAE']:,.0f}  ({elapsed:.1f}s)")
        return self

    # ── Stage 5: Evaluate ────────────────────────────────────────────────
    def evaluate(self):
        print("\n[5/6] Evaluation Summary:")
        print(f"  {'Model':<25} {'MAE':>10} {'RMSE':>10} {'R²':>8} {'Time':>8}")
        print("  " + "-" * 60)
        for name, m in self.results.items():
            print(f"  {name:<25} ${m['MAE']:>9,.0f} ${m['RMSE']:>9,.0f} {m['R2']:>8.4f} {m['time']:>6.1f}s")
        best = max(self.results, key=lambda k: self.results[k]['R2'])
        print(f"\n  ✅ Best model: {best}  (R² = {self.results[best]['R2']:.4f})")
        return self

    # ── Stage 6: Serialise ───────────────────────────────────────────────
    def save(self):
        os.makedirs(self.model_dir, exist_ok=True)
        joblib.dump(self.models['Linear Regression'], f'{self.model_dir}/linear_regression.pkl')
        joblib.dump(self.models['Random Forest'],     f'{self.model_dir}/random_forest.pkl')
        joblib.dump(self.models['Gradient Boosting'], f'{self.model_dir}/gradient_boosting.pkl')
        joblib.dump(self.scaler,                      f'{self.model_dir}/scaler.pkl')
        joblib.dump(self.top_features,                f'{self.model_dir}/features.pkl')
        print(f"[6/6] Models saved      : {self.model_dir}/")
        return self

    # ── Convenience ──────────────────────────────────────────────────────
    def run(self):
        return (self.load().preprocess().select_features().train().evaluate().save())

