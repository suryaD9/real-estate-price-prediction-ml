mae_rf_t    = mean_absolute_error(y_test, y_pred_rf_t)
rmse_rf_t   = np.sqrt(mean_squared_error(y_test, y_pred_rf_t))
r2_rf_t     = r2_score(y_test, y_pred_rf_t)
print(f"\nTuned RF — MAE: ${mae_rf_t:,.0f} | RMSE: ${rmse_rf_t:,.0f} | R²: {r2_rf_t:.4f}")

# § 3.7.3  GridSearchCV — Gradient Boosting
param_grid_gb = {
    'n_estimators' : [200, 300],
    'learning_rate': [0.05, 0.1],
    'max_depth'    : [3, 5],
    'subsample'    : [0.8, 1.0]
}

gb_grid = GridSearchCV(
    GradientBoostingRegressor(random_state=42),
    param_grid=param_grid_gb,
    cv=3, scoring='r2', n_jobs=-1, verbose=1)
gb_grid.fit(X_train, y_train)

print("\nBest GB hyperparameters:", gb_grid.best_params_)
print(f"Best CV R²           : {gb_grid.best_score_:.4f}")

gb_best     = gb_grid.best_estimator_
y_pred_gb_t = gb_best.predict(X_test)
mae_gb_t    = mean_absolute_error(y_test, y_pred_gb_t)
rmse_gb_t   = np.sqrt(mean_squared_error(y_test, y_pred_gb_t))
r2_gb_t     = r2_score(y_test, y_pred_gb_t)
print(f"\nTuned GB — MAE: ${mae_gb_t:,.0f} | RMSE: ${rmse_gb_t:,.0f} | R²: {r2_gb_t:.4f}")

