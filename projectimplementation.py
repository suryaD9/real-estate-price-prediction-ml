mask = np.triu(np.ones_like(corr_matrix, dtype=bool))
sns.heatmap(corr_matrix, mask=mask, annot=True, fmt='.2f', cmap='coolwarm',
            center=0, linewidths=0.5, ax=ax, annot_kws={'size': 7})
ax.set_title('Figure 4: Correlation Heatmap of Dataset Features (§3.5 — Feature Selection)',
             fontsize=13, fontweight='bold', pad=15)
plt.tight_layout(); plt.show()

# Figure 5: Top correlated features with price
price_corr = corr_matrix['price'].abs().sort_values(ascending=False).drop('price')
print("Top 10 features correlated with price:")
