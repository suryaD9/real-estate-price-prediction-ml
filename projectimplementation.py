# Prediction function — simulates real-time dashboard input (§3.9.1)
def predict_property_price(input_dict):
    """
    Real-time property price prediction system.
    Accepts a dictionary of property features and returns predictions
    from all three trained regression models.
    """
    lr_m   = joblib.load('models/linear_regression.pkl')
    rf_m   = joblib.load('models/random_forest.pkl')
    gb_m   = joblib.load('models/gradient_boosting.pkl')
    sc     = joblib.load('models/scaler.pkl')
    feats  = joblib.load('models/features.pkl')

    df_in = pd.DataFrame([input_dict])
    for f in feats:
        if f not in df_in.columns:
            df_in[f] = 0
    df_in = df_in[feats]
    df_sc = sc.transform(df_in)

    return {
        'Linear Regression':          round(lr_m.predict(df_sc)[0], 2),
        'Random Forest (Tuned)':      round(rf_m.predict(df_in)[0], 2),
        'Gradient Boosting (Tuned)':  round(gb_m.predict(df_in)[0], 2)
    }

