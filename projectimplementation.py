axes[0].yaxis.set_major_formatter(mticker.FuncFormatter(lambda x, _: f'${x/1e6:.1f}M'))
axes[1].boxplot(df_clean['price'], patch_artist=True, boxprops=dict(facecolor='#5cb85c', alpha=0.6))
axes[1].set_title('After Outlier Removal'); axes[1].set_ylabel('Price (GBP)')
axes[1].yaxis.set_major_formatter(mticker.FuncFormatter(lambda x, _: f'${x/1e6:.1f}M'))
plt.suptitle('Figure 3: Outlier Detection — Property Price (IQR Method, §3.4.5)', fontsize=13, fontweight='bold')
plt.tight_layout(); plt.show()

"""---
## Section 4: Feature Selection
*§ 3.5 — Correlation Analysis & Random Forest Feature Importance*
"""

# Define feature matrix and target
TARGET   = 'price'
FEATURES = [c for c in df_clean.columns if c != TARGET]
X = df_clean[FEATURES]
