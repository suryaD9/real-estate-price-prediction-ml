"""---
## Section 3: Data Preprocessing
*§ 3.4.1–3.4.5 — Data Cleaning, Missing Values, Encoding, Scaling, Outlier Detection*
"""

# § 3.4.1  Data Cleaning — Remove ID column & duplicates
df_clean = df.copy()
print(f"Rows before cleaning : {len(df_clean):,}")
df_clean.drop(columns=['id'], inplace=True)
before = len(df_clean)
df_clean.drop_duplicates(inplace=True)
print(f"Duplicates removed   : {before - len(df_clean)}")
print(f"Rows after cleaning  : {len(df_clean):,}")

# § 3.4.2  Handling Missing Values — median (numerical) / mode (categorical)
print("Missing values per column:")
mv = df_clean.isnull().sum()
print(mv[mv > 0] if mv.sum() > 0 else "  → No missing values detected ✅")

for col in df_clean.columns:
    if df_clean[col].dtype in ['float64','int64']:
        df_clean[col].fillna(df_clean[col].median(), inplace=True)
    else:
        df_clean[col].fillna(df_clean[col].mode()[0], inplace=True)
print("After imputation — missing values:", df_clean.isnull().sum().sum())

# § 3.4.3  Encoding Categorical Variables
print("Categorical columns:", df_clean.select_dtypes(include='object').columns.tolist())

# date → year and month features
df_clean['sale_year']  = pd.to_datetime(df_clean['date'], format='%Y%m%dT%H%M%S', errors='coerce').dt.year
df_clean['sale_month'] = pd.to_datetime(df_clean['date'], format='%Y%m%dT%H%M%S', errors='coerce').dt.month
df_clean.drop(columns=['date'], inplace=True)

# Binary encoding: waterfront
df_clean['waterfront'] = df_clean['waterfront'].map({'Y': 1, 'N': 0})

# Ordinal encoding: condition
condition_order = {'Poor': 0, 'Fair': 1, 'Average': 2, 'Good': 3, 'Very Good': 4}
df_clean['condition'] = df_clean['condition'].map(condition_order)

print("\nEncoding complete ✅")
print(df_clean.dtypes)

# § 3.4.5  Outlier Detection & Removal — IQR Method (multiplier = 3.0)
def remove_outliers_iqr(dataframe, column, multiplier=3.0):
    Q1, Q3 = dataframe[column].quantile(0.25), dataframe[column].quantile(0.75)
    IQR = Q3 - Q1
    lower, upper = Q1 - multiplier * IQR, Q3 + multiplier * IQR
    before = len(dataframe)
    dataframe = dataframe[(dataframe[column] >= lower) & (dataframe[column] <= upper)]
    print(f"  {column:20s}: removed {before - len(dataframe):4d} outliers  (bounds: [{lower:,.0f}, {upper:,.0f}])")
    return dataframe

print("Outlier removal (IQR × 3.0):")
for col in ['price', 'sqft_living', 'sqft_lot', 'bedrooms', 'bathrooms']:
    df_clean = remove_outliers_iqr(df_clean, col)

print(f"\nFinal dataset shape: {df_clean.shape[0]:,} rows × {df_clean.shape[1]} columns")

# Figure 3: Outlier detection — before vs after
fig, axes = plt.subplots(1, 2, figsize=(12, 5))
axes[0].boxplot(df['price'], patch_artist=True, boxprops=dict(facecolor='#d9534f', alpha=0.6))
